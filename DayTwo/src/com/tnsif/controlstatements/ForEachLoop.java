package com.tnsif.controlstatements;

public class ForEachLoop {
	public static void main(String[] args) {
		int a[] = {10,20,30,40,50};
		
		
		//For Each
		for (int i:a)
		{
			System.out.println(i);
		}
		
		String name[] = {"Rutuja","Swati","Mansi"};
		for (String n:name)
		{
			System.out.println(n);
		}

	}



}
